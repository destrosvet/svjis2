from django import template
from django.contrib.staticfiles import finders
from django.utils.safestring import mark_safe
from django.utils.translation import gettext as gt
from django.conf import settings
from articles.model_utils import PICTURE_ICONS, VIDEO_ICONS
import os
import re
from re import IGNORECASE, compile

register = template.Library()


@register.simple_tag
def static_version(path):
    """Cache-busting suffix for a static file, based on its own last-modified time."""
    found = finders.find(path) or os.path.join(settings.STATIC_ROOT or '', path)
    try:
        return int(os.path.getmtime(found))
    except OSError:
        return 0


@register.filter()
def highlight(text, search):
    if search == '':
        return text
    rgx = compile(f'({search})(?![^<>]*>)', IGNORECASE)
    highlighted = rgx.sub(lambda m: f'<b style="color:black;background-color:#ffff66">{m.group()}</b>', text)
    return mark_safe(highlighted)


@register.filter()
def highlight_exact(text, search):
    if search == '':
        return text
    if search == text:
        highlighted = f'<b style="color:black;background-color:#ffff66">{text}</b>'
    else:
        highlighted = text
    return mark_safe(highlighted)


@register.filter()
def inject_pictures(text, assets):
    for a in assets:
        file = a.file
        basename = a.basename
        _file_name, file_extension = os.path.splitext(basename)
        file_extension = file_extension[1:].lower()
        if file_extension in PICTURE_ICONS:
            text = text.replace(
                '{' + basename + '}', f'<img class="article-img" src="/media/{file}" alt="{basename}">'
            )
        if file_extension in VIDEO_ICONS:
            text = text.replace(
                '{' + basename + '}',
                f'<video class="article-video" controls><source src="/media/{file}"  alt="{basename}'
                f' type="video/{file_extension}" />Your browser does not support the video tag.</video>',
            )
    return mark_safe(text)


@register.filter()
def replace_url_to_link(value):
    # Replace url to link
    urls = re.compile(r"((https?):((//)|(\\\\))+[\w\d:#@%/;$()~_?\+-=\\\.&]*)", re.MULTILINE | re.UNICODE)
    value = urls.sub(r'<a href="\1" target="_blank">\1</a>', value)
    # Replace email to mailto
    urls = re.compile(r"([\w\-\.]+@(\w[\w\-]+\.)+[\w\-]+)", re.MULTILINE | re.UNICODE)
    value = urls.sub(r'<a href="mailto:\1">\1</a>', value)
    return mark_safe(value)


@register.filter()
def fault_editable(fault, user):
    return fault.can_be_edited_by(user)


@register.filter()
def yes_no(bool_value):
    result = gt("Yes") if bool_value else gt("No")
    return mark_safe(result)


